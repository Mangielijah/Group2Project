<h1 style="font-size: 40px; color: black; text-align: center; " >10 HEALTH AND NUTRITION TIPS THAT ARE ACTUALLY EVIDENCE-BASED </h1><br/>
<p style="font-size: 25px; font-weight: bold; margin-left: 30px;">1. DO NOT DRINK SUGAR CALORIES</p>
<p style="font-size: 20px;  font-weight: bold;  text-align: justify; font-family: verdana; margin-left: 30px;">Sugary drinks are the most fattening things you can put into your body.
This is because liquid sugar calories don't get registered by the brain in the same way as caloies from solid foods.
For this reason, when you drink soda, you end up eating more total calories.
Sugar drinks are strongly associated with obesity, type 2 diabetes, heart disease and all sorts of health problems.
Keep in mind that fruit juices are almost as bad as soda in this regard. They contain just as much sugar, and the small amounts of antioxidants do NOT negate the harmful effects of sugar.</p>

<p style="font-size: 25px; font-weight: bold; margin-left: 30px;">2. EAT NUTS</p>
<p style="font-size: 20px;  font-weight: bold;  text-align: justify; font-family: verdana; margin-left: 30px;">Despite being high in fat, nuts are incredily nutritious and healthy.
They are loaded with magnesium, vitamin E, fibre and various other nutrients.
Studies show that nuts can help you lose weight, and may help fight type 2 diabetes and heart disease.
Additionally, about 10-15% of the calories in nuts aren't even absorbed into the body, and some evidence suggests that they can boost metabolism.
In one study, almonds were shown to increase weight loss by 62% compared to complex carbohydrates.</p>

<p style="font-size: 25px; font-weight: bold; margin-left: 30px;">3. AVOID PROCESSED JUNK FOOD (Eat Real Food Instead)</p>
<p style="font-size: 20px;  font-weight: bold;  text-align: justify; font-family: verdana; margin-left: 30px;">All the processed junk food in the diet are the biggest reason the world is fatter and sicker than ever before.
These foods have been engineered to be "hyper-rewarding", so they trick our brains into eating more than we need, even reading to addition in some people.
They are also low in fibre, protein and micronutrients (empty calories), but high in unhealthy ingredients like added sugar and refined grains.</p>

<p style="font-size: 25px; font-weight: bold; margin-left: 30px;">4. DO NOT FEAR COFFEE</P>
<P style="font-size: 20px;  font-weight: bold;  text-align: justify; font-family: verdana; margin-left: 30px;">Coffee has been unfairly demonized. The truth is that it's actually very healthy.
Coffee is high in antioxidants, and studies show that coffee drinkers live longer, and have a reduced risk of type 2 diabetes, Parkinson's disease, Alzheimer's and numerous other diseases.</p>

<p style="font-size: 25px; font-weight: bold; margin-left: 30px;">5. EAT FATTY FISH</P>
<P style="font-size: 20px;  font-weight: bold;  text-align: justify; font-family: verdana; margin-left: 30px;">Pretty much everyone agrees that fish is healthy.
This is particularly true of fatty fish, like salmon, which is loaded with omega-3 fatty acids and various other nutrients.
Studies show that people who eat the most fish have a lower risk of all sorts of diseases, including heart disease, dementi and depression.</p>


<p style="font-size: 25px; font-weight: bold; margin-left: 30px;">6. GET ENOUGH SLEEP</p>
<p style="font-size: 20px;   font-weight: bold; text-align: justify; font-family: verdana; margin-left: 30px;">The importance of getting enough quality sleep can not be overrstated.
It may be just as important as diet and exercise, if not more
Poor sleep can drive insulin resistance, throw your appetite hoibrermones out of whack and reduce your physical and mental performance.
What's more, it is one of the strongest individual risk factors for future weight gain and obesity. One study tshowed that short sleep was linked to 89% increased risk of obesity in children, and 55% in adults.</p>

<p style="font-size: 25px; font-weight: bold; margin-left: 30px;">7. TAKE CARE OF YOUR GUT HEALTH WITH PROBIOTICS AND FIBER</p>
<p style="font-size: 20px;  font-weight: bold;  text-align: justify; font-family: verdana; margin-left: 30px;">The bacteria in your gut, collectively called the gut microbiota, are sometimes referred to as the "forgotten organ".		
These gut bugs are incredibly important for all sorts of health-related aspects. A disruption in the gut bacteria is linked to some of the world's most serious chronic diseases, including obesity.
A good way to improve gut health, is to eat probiotic foods like live yogurt and sauerkraut, take probiotic supplements, eat plenty of fiber. Fiber functions as fuel or the gut bacteria. </p>

<p style="font-size: 25px; font-weight: bold; margin-left: 30px;">8. DRINK SOME WATER, ESPECIALLY BEFORE MEALS</p>
<p style="font-size: 20px;  font-weight: bold;  text-align: justify; font-family: verdana; margin-left: 30px;">Drinking enough water can have numerous benefits.
One important factor, is if that it can help boost the amount of calories you burn.
According to 2 studies, it can boost metabolism by 24-34% over a period of 1-1.5 hours. This can amount to 96 additional calories burned if you drink 2 litres (67oz) of water per day.
The best time to drink water is half an hour before meals. One study showed that half a liter of water, 30minutes before each meal, increased weight loss by 44%.</p>

<p style="font-size: 25px; font-weight: bold; margin-left: 30px;">9. DO NOT OVERCOOK OR BURN YOUR MEAT</p>
<P style="font-size: 20px;  font-weight: bold;  text-align: justify; font-family: verdana; margin-left: 30px;">Meat can be a nutritious and healthy part of the diet. It is very high in protein, and contains various important nutrients.
The problems occur when meat is overcooked and burnt. This can lead to the formation of harmful compounds that raise the risk of cancer.
So, eat your meat, just don't overcook or burn it.</p>

<p style="font-size: 25px; font-weight: bold; margin-left: 30px;">10. AVOID BRIGHT LIGHTS BEFORE SLEEP</p>
<p style="font-size: 20px;  font-weight: bold;  text-align: justify; font-family: verdana; margin-left: 30px;">When we're exposed to bight lights in the evening, this disrupts production of the sleep hormone melatonin.
An interesting "hack" is to use a pair of amber-tinted glasses that block blue light from entering your eyes in  the evening.
This allows melatonin to be produced as if it were completely dark, helping you sleep better.</p>
